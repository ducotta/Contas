public class ContasNaoConfiavel {
	
	public double calculaQuadrado2(double numero){
		double quadrado = numero + numero;
		return quadrado;
	}
	
	public double calculaCubo2(double numero){
		double cubo = numero + numero + numero;
		return cubo;
	}

}
