package contas;

//Exercício 3 do capítulo 4 da apostila Introdução Java

public class Contas {
	
	public double calculaQuadrado(double numero){
		double quadrado = numero * numero;
		return quadrado;
	}
	public double calculaCubo(double numero){		
		double cubo = numero * numero * numero;
		return cubo;
	}

}
